class Destination {
  final String title;
  final String country;
  final String image;
  final double rating;
  final String description;
  final double price;
  final int reviews;
  final List<String> gallery;

  Destination({
    required this.title,
    required this.country,
    required this.image,
    required this.rating,
    required this.description,
    required this.price,
    required this.reviews,
    required this.gallery,
  });
}
